import { writeFileSync } from "node:fs";

import { read } from "./test-helpers.mjs";

const docxSource = read("src-tauri/src/adapters/docx/placeholders.rs");
const pdfSource = read("src-tauri/src/adapters/pdf.rs");

function extractLabels(source, pattern, domain) {
  const labels = [...source.matchAll(pattern)].map((match) => match[1]);
  if (labels.length === 0) {
    throw new Error(`未解析到 ${domain} 占位符常量`);
  }
  return labels;
}

function formatLabels(labels) {
  return labels.map((label) => `  "${label}"`).join(",\n");
}

const docxLabels = extractLabels(
  docxSource,
  /DOCX_[A-Z_]+_PLACEHOLDER:\s*&str\s*=\s*"\[([^\]"]+)\]";/g,
  "DOCX"
);
const pdfLabels = extractLabels(
  pdfSource,
  /PDF_[A-Z_]+_PLACEHOLDER:\s*&str\s*=\s*"\[([^\]"]+)\]";/g,
  "PDF"
);

const output = `// Auto-generated by scripts/generate-placeholder-labels.mjs
// Do not edit manually.
export const DOCX_PLACEHOLDER_LABELS = [
${formatLabels(docxLabels)}
] as const;

export const PDF_PLACEHOLDER_LABELS = [
${formatLabels(pdfLabels)}
] as const;
`;

writeFileSync(
  new URL("../src/lib/protectedTextPlaceholderLabels.generated.ts", import.meta.url),
  output,
  "utf8"
);

console.log("[generate-placeholder-labels] OK");
